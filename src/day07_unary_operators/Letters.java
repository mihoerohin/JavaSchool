package day07_unary_operators;

public class Letters {
    public static void main(String[] args) {


        char letter1 = 'M';
        char letter2 = 'i';
        char letter3 = 'h';
        char letter4 = 'o';

        System.out.println("Original " + letter1++);
        System.out.println("Original " + letter2++);
        System.out.println("Original " + letter3++);
        System.out.println("Original " + letter4++);
        System.out.println(letter1++);
        System.out.println(letter2++);
        System.out.println(letter3++);
        System.out.println(letter4++);



    }
}
