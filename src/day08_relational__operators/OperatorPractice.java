package day08_relational__operators;

public class OperatorPractice {
    public static void main(String[] args) {

        int b = 10;
        b++;  // b + 1
        System.out.println(b); // = 11
        b--; //  last b - 1
        System.out.println(b); // = 10
        System.out.println(b++); // show total before = 10
        System.out.println(b++); // last b + 1 = 11
        System.out.println(b); // last b++ + 1 = 12
        System.out.println(b); // last b = 12


    }
}
