package day08_relational__operators;

public class OperatorPractice2 {
    public static void main(String[] args) {


        int a = 40;
        System.out.println(--a); // a(40) - 1 = 39
        System.out.println(--a); // --a(39) - 1 = 38
        System.out.println(a--); // show me last = 38(and - 1 from last = 37)
        System.out.println(--a); // --a(37) - 1 = 36
        System.out.println(--a);  // --a(36) - 1 = 35
        System.out.println(a--); // show me last = 35(and - 1 from last = 34)
        System.out.println(a); // last a = 34



    }
}
